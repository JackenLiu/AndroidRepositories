package com.javareview.data;

public class 补码 {

    public static void main(String[] args) {
        /*
        00000000 00000000 00000000 10000011     131的二进制
        10000011                                变 byte
        10000010                                得到反码
        1 1111101                               源码 -125
         */

        byte a = 127;
        byte b = 4;
        byte c = (byte) (a + b);
        System.out.println(c);

        /*
        00000000 00000000 00000000 ‭01111101‬   -125源码
        11111111 11111111 11111111 10000010   -125反码
        11111111 11111111 11111111 10000011‬   -125补码 ；

        00000000 00000000 00000000 00000101‬   -5源码
        11111111 11111111 11111111 11111010   -5反码
        11111111 11111111 11111111 11111011‬   -5补码 ；

        11111111 11111111 11111111 10000011
      + 11111111 11111111 11111111 11111011
        ------------------------------------
      1 11111111 11111111 11111111 01111100   补码计算结果 ；

        01111100                              补码计算结果转 byte，结果为正数，这里就不转原码了：126
         */

        byte a1 = -125;
        byte b1 = -5;
        byte c1 = (byte) (a1 + b1);
        System.out.println(c1);
    }
}
